#include "libft.h"

void ft_lstadd_back(t_list **lst, t_list *new)
{
	if (!new)
		return ;
	if (!*lst)
	{
		*lst = new;
		return ;
	}
	t_list *temp;
	temp = ft_lstlast(*lst);
	temp->next = new;
	new->next = NULL;
	return ;
}
